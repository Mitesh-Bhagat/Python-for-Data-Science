# 🔢 Arithmetic Operators
a = 10
b = 3

print("Addition (+):", a + b)
print("Subtraction (-):", a - b)
print("Multiplication (*):", a * b)
print("Division (/):", a / b)
print("Floor Division (//):", a // b)
print("Modulus (%):", a % b)
print("Exponentiation (**):", a ** b)

# 🔍 Comparison Operators
print("Greater than (>):", a > b)
print("Less than (<):", a < b)
print("Equal to (==):", a == b)
print("Not equal (!=):", a != b)
print("Greater or equal (>=):", a >= b)
print("Less or equal (<=):", a <= b)

# 🔐 Logical Operators
x = True
y = False

print("AND:", x and y)
print("OR:", x or y)
print("NOT:", not x)

# 🧪 Bitwise Operators
print("Bitwise AND (&):", a & b)
print("Bitwise OR (|):", a | b)
print("Bitwise XOR (^):", a ^ b)
print("Bitwise NOT (~):", ~a)
print("Left Shift (<<):", a << 1)
print("Right Shift (>>):", a >> 1)

# 🧵 Membership Operators
lst = [1, 2, 3, 10]
print("10 in list:", 10 in lst)
print("5 not in list:", 5 not in lst)

# 🧱 Identity Operators
print("a is b:", a is b)
print("a is not b:", a is not b)

# 💥 Special Note
# `$` is NOT a valid operator in Python
# Use variables, functions, or string formatting instead